#include "../includes/minishell.h"

int	main(void)
{
	char	*input;
	char	**tokens;

	while (1)
	{
		input = readline("minishell> ");
		if (!input)
		{
			printf("exit\n");
			break ;
		}
		if (*input)
			add_history(input);
		tokens = tokenize(input);
		if (tokens && !is_builtin(tokens))
			exec_command(tokens);
		free_tokens(tokens);
		free(input);
	}
	return (0);
}

